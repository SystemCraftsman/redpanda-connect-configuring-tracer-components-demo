// Step 5 – Generate a complete Docker Compose stack:
//   • Jaeger all-in-one  (tracing backend + UI)
//   • Redpanda Connect   (with Jaeger tracer pre-configured)
//
// This script writes two files to /tmp/rp-connect-demo/:
//   connect-pipeline.yaml  – Redpanda Connect config with Jaeger tracer
//   docker-compose.yml     – full stack definition
//
// Usage:
//   node examples/05-docker-full-stack.js
//   cd /tmp/rp-connect-demo && docker compose up
//
// Then open the Jaeger UI at http://localhost:16686

'use strict';

const path = require('path');
const fs   = require('fs');
const { buildJaegerConfig, writeConfigFile } = require('../src/index');

const outDir = path.join('/tmp', 'rp-connect-demo');
if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });

try {
  // ── 1. Build the Redpanda Connect pipeline config ──────────────────────
  // Inside the Compose network, Jaeger is reachable at "jaeger:6831" (UDP)
  const connectYaml = buildJaegerConfig(
    {
      input: `input:
  generate:
    mapping: 'root = {"event": "docker_demo", "id": uuid_v4(), "ts": now()}'
    interval: 2s
    count: 0`,

      pipeline: `pipeline:
  processors:
    - mapping: |
        root = this
        root.step = "processed"`,

      output: `output:
  stdout: {}`,
    },
    {
      // Inside the Docker Compose network, "jaeger" resolves to the Jaeger container
      agentAddress:  'jaeger:6831',
      flushInterval: '500ms',
      samplerType:   'const',
      samplerParam:  1,
      tags: { env: 'docker-compose', pipeline: 'full-stack-demo' },
    },
  );

  const connectConfigPath = path.join(outDir, 'connect-pipeline.yaml');
  writeConfigFile(connectConfigPath, connectYaml);

  // ── 2. Build docker-compose.yml ────────────────────────────────────────
  // ASSUMPTION: docker.redpanda.com/redpandadata/connect:latest accepts
  //             "run <config>" as its command.
  const image = process.env.REDPANDA_CONNECT_IMAGE || 'docker.redpanda.com/redpandadata/connect:latest';

  const composeYaml = `# Auto-generated by examples/05-docker-full-stack.js
# Run: docker compose up
version: '3.8'

services:
  jaeger:
    image: jaegertracing/all-in-one:latest
    ports:
      - "6831:6831/udp"   # Jaeger agent UDP (thrift compact)
      - "16686:16686"     # Jaeger UI
      - "14268:14268"     # Jaeger HTTP collector
    environment:
      COLLECTOR_OTLP_ENABLED: "true"

  redpanda-connect:
    image: ${image}
    depends_on:
      - jaeger
    volumes:
      - ./connect-pipeline.yaml:/connect/config.yaml:ro
    command: ["run", "/connect/config.yaml"]
    # The HTTP API port — use it for health checks
    ports:
      - "4195:4195"
`;

  const composePath = path.join(outDir, 'docker-compose.yml');
  fs.writeFileSync(composePath, composeYaml, 'utf8');
  console.log(`[compose] docker-compose.yml written to ${composePath}`);

  console.log('\n--- Next steps ---');
  console.log(`1.  cd ${outDir}`);
  console.log('2.  docker compose up');
  console.log('3.  Open http://localhost:16686 in your browser.');
  console.log('4.  Select "redpanda-connect" service → Find Traces.');
} catch (err) {
  console.error('Error generating Docker Compose stack:', err.message);
  process.exit(1);
}